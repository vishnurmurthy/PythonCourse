'''
Use three quotes to make
a 
multiline
comment
'''


#Prints Hello on a line
print("Hello")
#Prints Goodbye on a line
print("Goodbye")

#Prints HelloGoodbye (same line, no space)
print("Hello", end = "")
print("Goodbye")

#Prints Hello Goodbye (same line, space)
print("Hello", end = " ")
print("Goodbye")

